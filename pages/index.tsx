export async function getServerSideProps(context: any) {
  const session = await getSession(context);
  console.log(session);
  if (session) {
    return {
      redirect: {
        permanent: false,
        destination: '/projects',
      },
    };
  }

  const providers = await getProviders();
  return {
    props: { providers }
  };
}
